void main(){

  //addition integer data type
  int number1 = 20;
  int number2 = 30;
  print(number1 + number2);

  //addition double data type
  double number3 = 40.589;
  double number4 = 30;
  print(number3 + number4);

  //subtraction integer data type
  int number5 = 10;
  int number6 = 5;
  print(number5 - number6);

  //subtraction double data type
  double number7 = 10.69;
  double number8 = 6.35;
  print(number7 - number8);

  //multiplication integer data type
  int number9 = 10;
  int number10 = 10;
  print(number9 * number10);

  //multiplication double data type
  double number11 = 5.364;
  double number12 = 8.25;
  print(number11 * number12);

  //divide integer data type
  int number13 = 10;
  int number14 = 5;
  print(number13 / number14);

  //divide double data type
  double number15 = 10.56;
  double number16 = 2.36;
  print(number15 / number16);

  //average integer data type
  int number17 = 10;
  int number18 = 20;
  print((number17 + number18) / 2);

  //average double data type
  double number19 = 63.56;
  double number20 = 52.369;
  print((number19 + number20) / 2);
}